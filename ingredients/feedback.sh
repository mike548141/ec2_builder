# Beautifies the feedback to std_out
feedback () {
  case ${1} in
    title)
      echo ''
      echo '*********************************************************************************'
      echo '*                                                                               *'
      echo "*   ${2}"
      echo '*                                                                               *'
      echo '*********************************************************************************'
      echo ''
    ;;
    h1)
      echo ''
      echo '================================================================================'
      echo "    ${2}"
      echo '================================================================================'
      echo ''
    ;;
    h2)
      echo ''
      echo '================================================================================'
      echo "--> ${2}"
      echo '--------------------------------------------------------------------------------'
    ;;
    h3)
      echo '--------------------------------------------------------------------------------'
      echo "--> ${2}"
    ;;
    body)
      echo "--> ${2}"
    ;;
    error)
      echo ''
      echo '********************************************************************************'
      echo " *** Error: ${2}"
      echo ''
    ;;
    *)
      echo ''
      echo "*** Error in the feedback function using the following parameters"
      echo "*** P0: ${0}"
      echo "*** P1: ${1}"
      echo "*** P2: ${2}"
      echo ''
    ;;
  esac
}

